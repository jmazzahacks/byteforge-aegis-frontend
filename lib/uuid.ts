// Aegis identifiers are UUIDs. Proxy routes validate path params against this
// before forwarding, so junk never reaches the backend as an identifier.
const UUID_RE = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i;

export function isUuid(value: string): boolean {
  return UUID_RE.test(value);
}
